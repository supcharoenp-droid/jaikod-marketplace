/**
 * Detect best subcategory for a given category
 */
export function detectSubcategory(params: {
    categoryId: number
    title: string
    description?: string
    imageAnalysis?: string
    detectedObjects?: string[]
}): SubcategoryRecommendation | null {

    const { categoryId, title, description, imageAnalysis, detectedObjects } = params

    // Get category
    const category = CATEGORIES.find(c => c.id === categoryId)
    if (!category || !category.subcategories) {
        return null
    }

    // Normalize text
    const normalize = (text: string) => text.toLowerCase().trim().replace(/\s+/g, ' ')
    const titleNorm = normalize(title)
    const descNorm = normalize(description || '')
    const imageNorm = normalize(imageAnalysis || '')

    // Score each subcategory
    const scores = category.subcategories.map(sub => {
        const keywords = SUBCATEGORY_KEYWORDS[sub.id] || []
        let score = 0
        const matched: string[] = []

        // Check title (highest priority)
        keywords.forEach(kw => {
            const kwNorm = normalize(kw)
            if (titleNorm.includes(kwNorm)) {
                const isThai = /[‡∏Å-‡πô]/.test(kwNorm)
                // Thai has no word boundaries, so treat significant partial match as exact
                const isExactWord = new RegExp(`\\b${kwNorm}\\b`).test(titleNorm)

                if (isExactWord) {
                    score += 50
                } else if (isThai && kwNorm.length > 2) {
                    score += 50 // High confidence for Thai matched keywords
                } else {
                    score += 30
                }
                if (!matched.includes(kw)) {
                    matched.push(kw)
                }
            }
        })

        // üõë SYSTEMATIC NEGATIVE LOGIC (Optimized)
        const negatives = NEGATIVE_KEYWORDS[sub.id]
        if (negatives && negatives.length > 0) {
            if (negatives.some(n => titleNorm.includes(n))) {
                score -= 1000 // Heavy penalty to force exclusion
                // Special exception: "Lens Kit" (Camera + Lens)
                if (sub.id === 801 && (titleNorm.includes('kit') || titleNorm.includes('‡∏û‡∏£‡πâ‡∏≠‡∏°‡πÄ‡∏•‡∏ô‡∏™‡πå'))) {
                    score += 1000 // Restore score for Kit Lens bundles
                }
            }
        }

        // Check description
        keywords.forEach(kw => {
            const kwNorm = normalize(kw)
            if (descNorm.includes(kwNorm)) {
                score += 20
                if (!matched.includes(kw)) matched.push(kw)
            }
        })

        // Check image analysis
        keywords.forEach(kw => {
            const kwNorm = normalize(kw)
            if (imageNorm.includes(kwNorm)) {
                score += 40 // High weight for AI vision
                if (!matched.includes(kw)) matched.push(kw)
            }
        })

        // Check detected objects
        if (detectedObjects && detectedObjects.length > 0) {
            detectedObjects.forEach(obj => {
                const objNorm = normalize(obj)
                keywords.forEach(kw => {
                    const kwNorm = normalize(kw)
                    if (objNorm.includes(kwNorm) || kwNorm.includes(objNorm)) {
                        score += 25
                        if (!matched.includes(kw)) matched.push(kw)
                    }
                })
            })
        }

        // Check subcategory name match
        const subNameTh = normalize(sub.name_th)
        const subNameEn = normalize(sub.name_en)
        if (titleNorm.includes(subNameTh) || titleNorm.includes(subNameEn)) {
            score += 60 // Big bonus
            matched.push(sub.name_th)
        }

        // Calculate confidence (0-1)
        const confidence = Math.min(score / 100, 1)

        return {
            subcategoryId: String(sub.id),
            subcategoryName: sub.name_th,
            confidence,
            reasoning: matched.length > 0
                ? `‡∏û‡∏ö‡∏Ñ‡∏≥‡∏ó‡∏µ‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ô: ${matched.slice(0, 3).join(', ')}`
                : '‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ñ‡∏≥‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç',
            matchedKeywords: matched,
            score
        }
    })

    // Sort by score
    scores.sort((a, b) => (b.score || 0) - (a.score || 0))

    // Return best match if confidence > 0.3
    const best = scores[0]
    if (best && best.confidence >= 0.3) {
        console.log('üéØ Subcategory Detection:', {
            category: category.name_th,
            detected: best.subcategoryName,
            confidence: best.confidence,
            matched: best.matchedKeywords
        })
        return best
    }

    console.log('‚ö†Ô∏è No confident subcategory match')
    return null
}

/**
 * Get subcategory suggestions (top 3)
 */
export function getSubcategorySuggestions(params: {
    categoryId: number
    title: string
    description?: string
    imageAnalysis?: string
}): SubcategoryRecommendation[] {

    const { categoryId, title, description, imageAnalysis } = params

    const category = CATEGORIES.find(c => c.id === categoryId)
    if (!category || !category.subcategories) {
        return []
    }

    const normalize = (text: string) => text.toLowerCase().trim()
    const titleNorm = normalize(title)
    const descNorm = normalize(description || '')
    const imageNorm = normalize(imageAnalysis || '')

    const suggestions = category.subcategories.map(sub => {
        const keywords = SUBCATEGORY_KEYWORDS[sub.id] || []
        let score = 0
        const matched: string[] = []

        keywords.forEach(kw => {
            const kwNorm = normalize(kw)
            if (titleNorm.includes(kwNorm)) {
                score += 50
                matched.push(kw)
            }
            if (descNorm.includes(kwNorm)) {
                score += 20
            }
            if (imageNorm.includes(kwNorm)) {
                score += 40
            }
        })

        return {
            subcategoryId: String(sub.id),
            subcategoryName: sub.name_th,
            confidence: Math.min(score / 100, 1),
            reasoning: matched.length > 0
                ? `‡∏û‡∏ö: ${matched.slice(0, 2).join(', ')}`
                : '',
            matchedKeywords: matched
        }
    }).filter(s => s.confidence > 0.2)

    // Sort and return top 3
    return suggestions
        .sort((a, b) => b.confidence - a.confidence)
        .slice(0, 3)
}
